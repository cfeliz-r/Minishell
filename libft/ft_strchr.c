#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	a;

	if (s == NULL)
		return (NULL);
	a = c;
	while (*s)
	{
		if (*s == a)
		{
			return ((char *) s);
		}
		s++;
	}
	if (a == '\0')
		return ((char *) s);
	return (0);
}
